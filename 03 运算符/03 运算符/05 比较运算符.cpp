#include<iostream>
using namespace std;

int main5() {
	//比较运算符
	//==
	int a = 10;
	int b = 20;
	cout << (a == b) << endl;//优先级

	//!=
	cout << (a != b) << endl;

	//>
	cout << (a > b) << endl;

	//<
	cout << (a < b) << endl;

	//>=
	cout << (a >= b) << endl;

	//<=
	cout << (a <= b) << endl;

	system("pause");

	return 0;
}